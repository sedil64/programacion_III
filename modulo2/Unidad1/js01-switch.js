let dia = "domingo";

switch (dia){
    case "lunes":
        console.log("Laboral");
        break;
    case "martes":
        console.log("Navidad");
        break;
    case "miercoles":
        console.log("Hallowen");
        break;
    case "jueves":
        console.log("Laboral");
        break;
    case "viernes":
        console.log("Viernes Santo");
        break;
    case "sabado":
        console.log("Sabado de Gloria");
        break;
    case "domingo":
        console.log("No Laboral");
        break;
    default:
        console.log("Dia No valido");
}